// Increment
let c =3
// Post-increment
console.log("c++ = ", c++)  // ans: [3 + 1 = 4]pahila c ko value linxa ani 1 le plus garxa

// pre-increment
console.log("++c = ", ++c) //ans: [4] pahila plus garxa ani matra c ko value linxa

// Decrement
d = 5
// Post-increment
console.log("d-- = ", d--) //ans: [5 -1 = 4] pahila d ko value linxa ani 1 le minus garxa

// pre-increment
console.log("--d = ", --d) //ans: [3] pahila minus garxa ani matra d ko value linxa
