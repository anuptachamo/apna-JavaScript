//Assignment Operators
let assignment = 1;
assignment  += 5  //assignment = assignment + 5 (ans: 1 + 5 = 6)
console.log('ADD: ' ,assignment)

assignment  -= 5  //assignment = assignment - 5 (ans: 6 - 5 = 1)
console.log('Subtract: ' ,assignment)

assignment  *= 5  //assignment = assignment * 5 (ans: 1 * 5 = 5)
console.log('multiple: ' ,assignment)

assignment  /= 5  //assignment = assignment / 5 (ans: 5 / 5 = 1)
console.log('Divide: ' ,assignment)

assignment  %= 5  //assignment = assignment % 5 (ans: 1 % 5 = 1)
console.log('%: ' ,assignment)

assignment  **= 5  //assignment = assignment ** 5 (ans: 1^1 = 1)   --**(define a power)
console.log('Power of: ' ,assignment)