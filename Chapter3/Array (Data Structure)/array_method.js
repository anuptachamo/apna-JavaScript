/**
 *  ? Array Methods
 *      push : add to end
 *      Pop : delete from end and returns it
 *      Unshift : add to start
 *      Shift : delete from start and returns it
 */

let cars = ["maruti", "xyz"]

//push example
console.log(cars)
console.log(cars.push("toyota"))  //push le last ma add gardinxa
console.log(cars)

//pop example
console.log(cars.pop())  //pop le last dekhii delete garchha
console.log(cars)
console.log(cars.pop())


let place = ["bkt", "ktm"]

//unshift example
console.log(place)
console.log(place.unshift("banepa")) //unshift le first dekhii add gardinchha
console.log(place)

//shift example
console.log(place.shift())
console.log(place)


