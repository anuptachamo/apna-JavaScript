/**
 *  ? Array Methods
 *      *slice : copies a portion of an array
 */

let colors = ["red", "yellow", "blue", "orange", "pink", "white"]
console.log(colors.slice())   //print all output

// starting form array index 2
console.log(colors.slice(2))

//starting from array index 2 and end on index 2
console.log(colors.slice(2, 3))

//print output from last
console.log(colors.slice(-2))