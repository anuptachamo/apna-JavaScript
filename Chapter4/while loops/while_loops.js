/**
 *  ?while loop
 *      while (condition){
 *          do something
 *      }
 * 
 *      let i = 1;
 *      while (i <=5){
 *          console.log(i)
 *          i++;
 *      }
 * 
 *      Initialization, Condition Check, Loop Body, Update/Modification, 
 *      and back to Condition Check. This process continues until the condition becomes false.
 */

let i = 1;
while (i <=5){
    console.log(i)
    i++;
}

