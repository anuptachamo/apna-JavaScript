/**
 *  ?Loops
 *      * Used to iterate a piece of code
 * 
 *      ?for loop
 *          for (initialization; condition; updation){
 *                  do something
 *              }
 *          
 *              for (let i = 1; 1<=5; i ++){
 *                  console.log(i);
 *              }
 */

//checking forward
for (let i = 1; i<=5; i++){
    console.log(i)
}

//checking backward
for (let j = 5; j >=1; j--){
    console.log(j)
}

for (let k = 5; k >=1; k = k-2){
    console.log(k)
}